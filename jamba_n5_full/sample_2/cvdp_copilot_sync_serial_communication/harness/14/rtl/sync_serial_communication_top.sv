`timescale 1ns / 1ps
module sync_serial_communication_tx_rx(
    input clk,                     // Clock signal
    input reset_n,                 // Active low reset signal
    input [2:0] sel,               // Selection signal for TX block
    input [63:0] data_in,          // 64-bit data input for TX block
    output [63:0] data_out,        // 64-bit data output from RX block
    output done                    // Done signal indicating completion
);

// Internal signals
wire tx_out;                       // Serial data output from TX block
wire tx_done;                      // Done signal from TX block
wire serial_clk_in;                // Serial clock generated by TX block

// Tx‑Block Modifications
module tx_block(
    input clk,                     // Clock input
    input reset_n,                 // Active-low reset input
    input [63:0] data_in,          // 64-bit parallel data input
    input [2:0] sel,               // Selection input to choose data width
    output reg serial_out,         // Serial data output
    output reg done,               // Done signal indicating completion of transmission
    output serial_clk,             // Clock for serial data transmission
    output parity_out              // New parity output
);

    // Internal registers
    reg [63:0] data_reg;
    reg [6:0] bit_count;
    reg [6:0] reg_count;
    reg [6:0] temp_reg_count;
    reg [7:0] parity_reg;
    reg parity_in, parity_error;

    // Sequential block for state control and data selection
    always @(posedge clk or negedge reset_n) begin
        if (!reset_n) begin
            // Reset all values on active-low reset
            data_reg     <= 64'h0;
            bit_count    <= 7'h0;
            parity_reg   <= 8'h0;
            parity_in    <= 1'b0;
            parity_error  <= 1'b1;
        end else begin
            if (sel == 3'b000) begin
                data_reg <= 64'h0;
                bit_count <= 7'h0;
            end else if (sel == 3'b001) begin
                data_reg <= {56'h0, data_in[7:0]};
                bit_count <= 7'd7;
            end else if (sel == 3'b010) begin
                data_reg <= {48'h0, data_in[15:0]};
                bit_count <= 7'd15;
            end else if (sel == 3'b011) begin
                data_reg <= {32'h0, data_in[31:0]};
                bit_count <= 7'd31;
            end else if (sel == 3'b100) begin
                data_reg <= data_in[63:0];
                bit_count <= 7'd63;
            end default: data_reg <= 64'h0;

            // Compute parity
            assign parity_reg = xor_comb(data_reg);

            // Assign parity_out
            assign parity_out = parity_reg;
        end

    end

    // Generate serial clock
    assign #1 serial_clk = clk && (temp_reg_count !== 7'd0) ;  // Placeholder for simulation

    // Register to keep track of reg_count
    always@(posedge clk or negedge reset_n) begin 
        if(!reset_n) begin
            temp_reg_count <= 7'h0;
        end
        else begin
            temp_reg_count <= reg_count;
        end
    end

    // Sequential logic to drive the serial_out signal
    always@(posedge clk or negedge reset_n) begin 
        if(!reset_n) begin
            serial_out <= 1'b0;
        end
        else if(reg_count > 7'h0 || bit_count > 7'h0) begin
            serial_out <= data_reg[0];
        end
    end

    // Set the done signal
    always@(posedge clk or negedge reset_n) begin
        if(!reset_n) begin
            done <= 1'b0;
        end
        else begin
            if(bit_count == 7'h0) begin
                done <= 1'b1;
            end
            else begin
                done <= 1'b0;
            end
        end
    end

endmodule

// Rx‑Block
module rx_block(
    input wire clk,  			    // clock input
    input wire reset_n,              // Active-low reset
    input wire data_in,              // Serial input data
    input wire serial_clk,           // Clock signal for serial data
    input wire [2:0] sel,            // Selection output to choose data width
    output reg done,                 // Done signal to indicate data reception complete
    output reg [63:0] data_out,      // Parallel output data after serial operation
    output reg parity_in,            // Input parity bit
    output reg parity_error          // Parity error flag
);

    // Internal registers
    reg [63:0] data_reg;
    reg [7:0] bit_count;
    reg [7:0] count;

    // Sequential logic block to capture serial data
    always @(posedge serial_clk or negedge reset_n) begin
        if (!reset_n) begin
            data_reg <= 64'h0;
            bit_count <= 8'd0;
        end else begin
            if(done == 1'b1) begin
                bit_count <= 8'b0;
                data_reg <= 64'h0;
            end
            else begin
                bit_count <= bit_count;
                data_reg[bit_count] <= data_in;
            end
        end
    end

    // Monitor completion
    always@(posedge clk or negedge reset_n) begin
        if(!reset_n) begin
            count <= 8'd0;
            done <= 1'b0;
            data_out <= 64'h0;
        end
        else if(count >= bit_count && count != 8'd0) begin
            done <= 1'b1;
            case(sel)
                3'b000: begin
                    data_out <= 64'h0;
                end
                3'b001: begin
                    data_out <= {56'h0, data_reg};
                end
                3'b010: begin
                    data_out <= {48'h0, data_reg};
                end
                3'b011: begin
                    data_out <= {32'h0, data_reg};
                end
                3'b100: begin
                    data_out <= data_reg;
                end
                default: begin
                    data_out <= 64'h0;
                end
            end
        end
    end

    // Capture incoming parity
    always @(posedge serial_clk or negedge reset_n) begin
        if (!reset_n) begin
            parity_in <= 1'b0;
        end else begin
            parity_in <= data_in;
        end
    end

    // Compute computed parity
    assign computed_parity = xor_comb(data_reg);

    // Parity error detection
    always @(posedge serial_clk or negedge reset_n) begin
        if (!reset_n) begin
            parity_error <= 1'b1;
        end else if (parity_in !== computed_parity) begin
            parity_error <= 1'b1;
        else
            parity_error <= 1'b0;
        end
    end

    // Top‑level outputs
    assign parity_error = parity_in !== computed_parity;
    assign parity_in = data_in;
    assign data_out = data_reg;

endmodule
