module sync_serial_communication_top (
    input clk,               // Clock signal
    input reset_n,           // Active-low reset input
    input [2:0] sel,         // Selection input
    input [63:0] data_in,    // 64-bit data input
    output [63:0] data_out,  // 64-bit data output
    output done               // Done signal
);

// Internal signals
wire tx_out;               // Serial data output from TX block
wire tx_done;             // Done signal from TX block
wire serial_clk_in;       // Serial clock generated by TX block
wire rx_data;             // Serial data input from RX block
wire rx_done;             // RX done signal
wire rx_serial_clk;       // RX serial clock

// Instantiate the TX block
tx_block uut_tx_block (
    .clk(clk),
    .reset_n(reset_n),
    .data_in(data_in),
    .sel(sel),
    .serial_out(tx_out),
    .done(tx_done),
    .serial_clk(serial_clk_in)
);

// Instantiate the RX block
rx_block uut_rx_block (
    .clk(clk),
    .serial_clk(rx_serial_clk),
    .reset_n(reset_n),
    .sel(sel),
    .data_in(rx_data),
    .data_out(data_out),
    .done(rx_done)
);

endmodule

///// tx_block
module tx_block (
    input clk,               // Clock input
    input reset_n,           // Active-low reset input
    input [63:0] data_in,    // 64-bit data input
    input [2:0] sel,         // Selection input
    output reg data_reg,     // Data register
    output reg done,         // Done signal
    output reg serial_clk    // Serial clock
);

// Internal registers
reg [63:0] data_reg;       // Data register
reg [6:0] bit_count;        // Bit counter
reg [6:0] reg_count;        // Register counter
reg [6:0] temp_reg_count;   // Temporary register

// Sequential block for state control
always @(posedge clk or negedge reset_n) begin
    if (!reset_n) begin
        data_reg     <= 64'h0;
        bit_count    <= 7'h0;
        reg_count    <= 7'h0;
    else begin
        if (done == 1'b1) begin
            case (sel)
                3'b000: begin
                    data_reg  <= 64'h0;
                    bit_count <= 7'd0;
                end
                3'b001: begin
                    data_reg  <= {56'h0, data_in[7:0]};
                    bit_count <= 7'd7;
                end
                3'b010: begin
                    data_reg  <= {48'h0, data_in[15:0]};
                    bit_count <= 7'd15;
                end
                3'b011: begin
                    data_reg  <= {32'h0, data_in[31:0]};
                    bit_count <= 7'd31;
                end
                3'b100: begin
                    data_reg  <= data_in[63:0];
                    bit_count <= 7'd63;
                end
                default: begin
                    data_reg  <= 64'h0;
                    bit_count <= 7'h0;
                end
            endcase
        end else if (bit_count > 7'h0) begin
            data_reg   <= data_reg >> 1;
            bit_count  <= bit_count - 1'b1;
        end
        reg_count <= bit_count;
    end
end

// Generate serial clock
assign serial_clk = (reg_count > 7'h0) && (temp_reg_count != 7'd0);

// Sequential logic to drive serial_out
always @(posedge clk or negedge reset_n) begin
    if (reg_count > 7'h0) begin
        serial_out <= data_reg[0];
    end
end

// Set done signal
always @(posedge clk or negedge reset_n) begin
    if (reg_count == 7'h0) begin
        done <= 1'b1;
    else begin
        done <= 1'b0;
    end
end
endmodule

///// rx_block
module rx_block (
    input wire clk,           // Clock input
    input wire reset_n,        // Active-low reset
    input wire rx_data,        // Serial input data
    input wire rx_serial_clk,  // Serial clock
    input wire [2:0] sel,      // Selection output
    output reg done,          // Done signal
    output reg [63:0] data_out // Parallel output data
);

// Internal registers
reg [63:0] data_reg;       // Data register
reg [7:0] bit_count;       // Bit counter
reg [7:0] count;            // General purpose counter

// Sequential logic block
always @(posedge rx_serial_clk or negedge reset_n) begin
    if (!reset_n) begin
        data_reg <= 64'h0;
        bit_count <= 8'd0;
    else begin
        if (done == 1'b1) begin
            bit_count <= 8'd0;
            data_reg <= 64'h0;
        else begin
            bit_count <= bit_count + 1'b1;
            data_reg[bit_count] <= rx_data;
        end
    end
end

// Sequential logic to monitor completion and update outputs
always @(posedge clk or negedge reset_n) begin
    if (!reset_n) begin
        count <= 8'd0;
        done <= 1'b0;
        data_out <= 64'h0;
    else if (count >= bit_count && count != 8'd0) begin
        done <= 1'b1;
        case (sel)
            3'b000: begin
                data_out <= 64'h0;
            end
            3'b001: begin
                data_out <= {56'h0, data_reg};
            end
            3'b010: begin
                data_out <= {48'h0, data_reg};
            end
            3'b011: begin
                data_out <= {32'h0, data_reg};
            end
            3'b100: begin
                data_out <= data_reg;
            end
            default: begin
                data_out <= 64'h0;
            end
        endcase
    else if (bit_count > 8'd0) begin
        count <= count + 1'b1;
        done <= 1'b0;
    else begin
        count <= 8'd0;
        done <= 1'b0;
    end
end

endmodule