module sync_serial_communication_top (
    input clk, 
    input reset_n, 
    input [2:0] sel, 
    input [63:0] data_in, 
    output [63:0] data_out, 
    output done, 
    output [63:0] data_out,
    output parity_error
)
// Internal signals
wire tx_out;                       // Serial data output from TX block
wire tx_done;                      // Done signal from TX block
wire serial_clk_in;                // Serial clock generated by TX block
wire parity;                       // Parity bit for TX block

// Instantiate the TX block
tx_block uut_tx_block (
    .clk(clk),                     // Clock input to TX block
    .reset_n(reset_n),             // Reset input to TX block
    .data_in(data_in),             // 64-bit data input to TX block
    .sel(sel),                     // Selection signal to TX block
    .serial_out(tx_out),           // Serial data output from TX block
    .done(tx_done),                // Done signal from TX block
    .serial_clk(serial_clk_in)     // Serial clock output from TX block
);

// Instantiate the RX block
rx_block uut_rx_block (
    .clk(clk),                     // Clock input to RX block
    .serial_clk(serial_clk_in),    // Serial clock input to RX block
    .reset_n(reset_n),             // Reset input to RX block
    .sel(sel),                     // Selection signal to RX block
    .data_in(tx_out),              // Serial data input to RX block
    .data_out(data_out),           // 64-bit data output from RX block
    .done(done),                   // Done signal from RX block
    .parity_in(parity_in)          // Parity bit from TX block
);

// Parity generation and checking logic
module tx_block (
    input clk,               // Clock input
    input reset_n,           // Active-low reset input
    input [63:0] data_in,    // 64-bit parallel data input
    input [2:0] sel,         // Selection input to TX block
    output reg parity,       // Parity bit for data transmission
    output reg serial_out,   // Serial data output
    output reg serial_clk,    // Clock for serial data transmission
    output reg tx_done        // Done signal indicating completion
);

// Internal registers
reg [63:0] data_reg;         // Register to hold the data being transmitted
reg [6:0] bit_count;         // Counter to track number of bits to transmit
reg [6:0] reg_count;         // Register for counting bits for serial clock control
reg [6:0] temp_reg_count;    // Temporary register to track reg_count

// Sequential block for state control and data selection
always @(posedge clk or negedge reset_n) begin
    if (!reset_n) begin
        data_reg     <= 64'h0;
        bit_count    <= 7'h0;
        reg_count    <= 7'h0;
    else begin
        if (done == 1'b1) begin
            case (sel)
                3'b000: begin
                    data_reg  <= 64'h0;
                    bit_count <= 7'd0;
                end
                3'b001: begin
                    data_reg  <= {56'h0, data_in[7:0]};
                    bit_count <= 7'd7;
                end
                3'b010: begin
                    data_reg  <= {48'h0, data_in[15:0]};
                    bit_count <= 7'd15;
                end
                3'b011: begin
                    data_reg  <= {32'h0, data_in[31:0]};
                    bit_count <= 7'd31;
                end
                3'b100: begin
                    data_reg  <= data_in[63:0];
                    bit_count <= 7'd63;
                end
                default: begin
                    data_reg  <= 64'h0;
                    bit_count <= 7'h0;
                end
            endcase
        end else if (bit_count > 7'h0) begin
            data_reg   <= data_reg >> 1;
            bit_count  <= bit_count - 1'b1;
        end
        reg_count <= bit_count;
    end
end

// Generate serial clock based on reg_count
assign serial_clk = (reg_count > 7'h0) && (temp_reg_count != 7'd0);

// Sequential logic to drive the serial_out signal based on reg_count and bit_count
always @(posedge clk or negedge reset_n) begin
    if (!reset_n) begin
        serial_out <= 1'b0;
    end else if (reg_count > 7'h0 || bit_count > 7'h0) begin
        serial_out <= data_reg[0];
    end
end

// Set the done signal when transmission is complete (bit_count reaches zero)
always @(posedge clk or negedge reset_n) begin
    if (!reset_n) begin
        done <= 1'b0;
    end else begin
        if (bit_count == 7'h0) begin
            done <= 1'b1;
        else begin
            done <= 1'b0;
        end
    end
end
endmodule

// Parity calculation for TX block
always @posedge sel[2] begin
    parity = 0;
    case (sel[1:0])
        3'b00: parity = data_in[7:0];
        3'b01: parity = data_in[15:0];
        3'b10: parity = data_in[31:0];
        default: parity = data_in[63:0];
    endcase
    parity = parity ^ parity; // Compute parity bit
end

// Parity validation and error detection in RX block
module rx_block (
    input wire clk,   // Clock input
    input wire reset_n, // Active-low reset
    input wire data_in, // Serial input data
    input wire parity_in, // Parity bit from TX
    input wire [7:0] bit_count, // Number of bits received
    input wire [6:0] reg_count, // Register counting bits
    input wire [6:0] temp_reg_count, // Temporary register
    output reg done, // Done signal to indicate data reception complete
    output reg [63:0] data_out, // Parallel output data after serial operation
    output reg parity_error // Parity error output
);

// Internal registers
reg [63:0] data_reg;    // Register to hold incoming serial data
reg [7:0] bit_count;    // Counter for the number of bits received
reg [7:0] count;        // General purpose counter to sync with bit_count

// Sequential logic block to capture serial data on the rising edge of serial_clk
always @(posedge serial_clk or negedge reset_n) begin
    if (!reset_n) begin
        data_reg <= 64'h0;
        bit_count <= 8'd0;
    end else begin
        if (done == 1'b1) begin
            bit_count <= 8'b0;
            data_reg <= 64'h0;
        end
        else begin
            bit_count <= bit_count + 1'b1;
            data_reg[bit_count] <= data_in;
        end
    end
end

// Sequential logic block to monitor the completion of data reception and update outputs
always @(posedge clk or negedge reset_n) begin
    if (!reset_n) begin
        count <= 8'd0;
        done <= 1'b0;
        data_out <= 64'h0;
    end else if (count >= bit_count && count != 8'd0) begin
        done <= 1'b1;
        case (sel)
            3'b000: begin
                data_out <= 64'h0;
            end
            3'b001: begin
                data_out <= {56'h0, data_reg};
            end
            3'b010: begin
                data_out <= {48'h0, data_reg};
            end
            3'b011: begin
                data_out <= {32'h0, data_reg};
            end
            3'b100: begin
                data_out <= data_reg;
            end
            default: begin
                data_out <= 64'h0;
            end
        endcase
    end else if (bit_count > 8'd0) begin
        count <= count + 1'b1;
        done <= 1'b0;
    end
    else begin
        count <= 8'd0;
        done <= 1'b0;
    end
end

// Parity error detection
always @posedge sel[2] begin
    parity_check = (data_reg & 0x80000000) ? 1 : 0;
    parity_error = (parity_in != parity_check);
end
endmodule