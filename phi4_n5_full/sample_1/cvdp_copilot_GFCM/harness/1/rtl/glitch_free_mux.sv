               if ( (current_clk == 1) && (clk2_en == 1) ) begin
                  // new clock enabled, complete switch
                  state <= STATE_NORMAL;
               end else if ( (current_clk == 0) && (clk1_en == 1) ) begin
                  // new clock enabled, complete switch
                  state <= STATE_NORMAL;
               end
            end
         endcase
      end
   end

   // Now generate the enables for clk2 in its own always block triggered by clk2
   always_ff @(posedge clk2 or negedge rst_n) begin
      if (!rst_n) begin
         clk2_en <= 0;
      end else begin
         if (state == STATE_SWITCH && current_clk == 0) begin
            // switching from clk1 to clk2: enable clk2 on first rising edge of clk2
            clk2_en <= 1;
         end else begin
            clk2_en <= 0;
         end
      end
   end

   // Generate clkout: use multiplexer controlled by enable signals
   // To be glitch free, ensure that only one enable is high at a time.
   always_comb begin
      if (!rst_n)
         clkout = 0;
      else if (clk1_en)
         clkout = clk1;
      else if (clk2_en)
         clkout = clk2;
      else
         clkout = 0;
   end

endmodule

This is a possible solution. However, I'm not entirely satisfied with the state machine. The specification says: "When sel changes from 0 to 1, clk1 is disabled on the first positive edge of clk1 after the sel change. Subsequently, clk2 is enabled on the first positive edge of clk2 after clk1 is disabled." So the switching sequence for 0->1 is: On rising edge of clk1, if sel changed to 1, then disable clk1. Then, on rising edge of clk2, enable clk2. Similarly, for 1->0: On rising edge of clk2, if sel changed to 0, then disable clk2. Then, on rising edge of clk1, enable clk1.

We can implement that more directly with two always blocks, one for clk1 and one for clk2, that share a common register "sel_reg". Let's try that:

