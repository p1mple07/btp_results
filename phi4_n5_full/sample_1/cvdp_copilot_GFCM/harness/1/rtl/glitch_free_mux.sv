This solution uses two always_ff blocks, one triggered by clk1 and one triggered by clk2, which share a common register sel_reg. However, note that sel_reg is being updated in both always blocks, which is problematic because it's written in two different clock domains. That is not synthesizable. We need a common clock domain for sel_reg. We can assume that sel is synchronous to one of the clocks, say clk1. Then we can update sel_reg only in the clk1 always block, and use that in the clk2 always block. But then how do we detect a transition in the clk2 always block? We can sample sel_reg from clk1 domain in clk2 domain, but then there will be a delay. But that's acceptable if the switching sequence is based on the delayed value.

Let's assume sel is synchronous to clk1. Then we can do:

module glitch_free_mux (
    input logic clk1,
    input logic clk2,
    input logic sel,
    input logic rst_n,
    output logic clkout
);

   logic clk1_en, clk2_en;
   logic sel_reg; // registered version of sel, updated on clk1 domain

   // Use clk1 as system clock for control logic
   always_ff @(posedge clk1 or negedge rst_n) begin
      if (!rst_n) begin
         sel_reg <= 0;
         clk1_en <= 1;
         clk2_en <= 0;
      end else begin
         // Normal operation: if sel is 0, clk1 should be enabled, if sel is 1, clk2 should be enabled.
         clk1_en <= (sel == 0);
         clk2_en <= (sel == 1);
         sel_reg <= sel;
      end
   end

   // In clk2 always block, detect transition from 0 to 1 based on sel_reg from previous cycle
   always_ff @(posedge clk2 or negedge rst_n) begin
      if (!rst_n) begin
         // do nothing, clk2_en already 0
      end else begin
         // On first rising edge of clk2 after sel change from 0 to 1, ensure clk2 is enabled.
         if (sel_reg == 0 && sel == 1) begin
            // This condition might not be true because sel_reg is updated on clk1 domain.
            // Instead, we can use a register that holds the previous value of sel_reg.
            // Let's add prev_sel_reg.
         end
      end
   end

   // Let's add prev_sel_reg to detect transition in clk2 domain.
   logic prev_sel_reg;

   // Update prev_sel_reg in clk1 always block
   always_ff @(posedge clk1 or negedge rst_n) begin
      if (!rst_n) begin
         prev_sel_reg <= 0;
      end else begin
         prev_sel_reg <= sel_reg;
      end
   end

   // Now, in clk2 always block, detect transition from 0 to 1.
   always_ff @(posedge clk2 or negedge rst_n) begin
      if (!rst_n) begin
         // do nothing
      end else begin
         if (prev_sel_reg == 0 && sel_reg == 1) begin
            // Transition detected: switching from clk1 to clk2.
            // On the first rising edge of clk2 after the transition, enable clk2.
            clk2_en <= 1;
            // Also ensure clk1_en is 0 (already done in clk1 always block).
         end
         // For transition from 1 to 0, we want to enable clk1 on clk1 domain, so nothing needed here.
      end
   end

   // Generate clkout
   always_comb begin
      if (!rst_n)
         clkout = 0;
      else if (clk1_en)
         clkout = clk1;
      else if (clk2_en)
         clkout = clk2;
      else
         clkout = 0;
   end

endmodule

This design uses clk1 as the system clock for control logic, and registers sel_reg and prev_sel_reg. Then in the clk2 always block, if a transition from 0 to 1 is detected (prev_sel_reg==0 and sel_reg==1), then on the first rising edge of clk2, set clk2_en to 1. Similarly, for transition from 1 to 0, clk1_en is set in the clk1 always block. This satisfies the specification: when sel changes from 0 to 1, clk1 is disabled on the first rising edge of clk1 (because clk1_en = (sel==0) becomes 0) and then clk2 is enabled on the first rising edge of clk2 (clk2_en is set to 1). Similarly, when sel changes from 1 to 0, clk2 is disabled (clk2_en = (sel==1) becomes 0) on clk2's rising edge? But wait, the specification says: "When sel changes from 1 to 0, clk2 is disabled on the first positive edge of clk2 after the sel change." But in our design, clk2_en is driven by (sel==1) in the clk1 always block, so when sel becomes 0, clk2_en becomes 0 on the next rising edge of clk1. But we want clk2 to be disabled on the rising edge of clk2, not clk1. But we cannot easily do that because sel is synchronous to clk1, not clk2. We might need to detect transition in clk2 domain as well. So we add a similar mechanism for transition from 1 to 0 in the clk1 always block? But clk1 always block is already triggered by clk1. So for transition from 1 to 0, we want clk1 to be enabled on the first rising edge of clk1 after the transition. And in our clk1 always block, clk1_en <= (sel==0) will do that. So that part is fine.

The tricky part is ensuring that when sel changes from 0 to 1, clk1 is disabled on clk1's rising edge (which is handled by clk1 always block because clk1_en = (sel==0) will be 0 when sel becomes 1) and clk2 is enabled on clk2's rising edge (which is handled by clk2 always block using prev_sel_reg and sel_reg). Similarly, when sel changes from 1 to 0, clk2 is disabled on clk2's rising edge? But our clk1 always block sets clk2_en = (sel==1), so when sel becomes 0, clk2_en will be 0 on the next rising edge of clk1, not clk2. We want clk2 disabled on clk2's rising edge. To do that, we need to detect transition in clk2 domain as well for 1->0. But sel is synchronous to clk1, so its value in clk2 domain might be delayed. We can sample sel in clk2 domain as well. Let's add a register for sel in clk2 domain, say sel_d, updated in clk2 always block. But then we have two domains. We can do:

In clk2 always block, sample sel (which is asynchronous to clk2, but that's fine if we assume it's metastability-free because it's synchronous to clk1 and clk2 are synchronous with fixed phase). But then we can detect transition from 1 to 0 in clk2 domain. So in clk2 always block, do:
   if (prev_sel_d == 1 && sel_d == 0) then disable clk2 (clk2_en <= 0) on rising edge of clk2.
And update sel_d and prev_sel_d in clk2 always block.

So overall, we can do:

module glitch_free_mux (
    input logic clk1,
    input logic clk2,
    input logic sel,
    input logic rst_n,
    output logic clkout
);

   logic clk1_en, clk2_en;
   logic sel_reg; // registered sel from clk1 domain
   logic prev_sel_reg; // previous registered sel from clk1 domain

   // clk1 domain: control logic and sel registration
   always_ff @(posedge clk1 or negedge rst_n) begin
      if (!rst_n) begin
         sel_reg <= 0;
         prev_sel_reg <= 0;
         clk1_en <= 1;
         clk2_en <= 0;
      end else begin
         // Normal operation: if sel is 0, enable clk1; if sel is 1, enable clk2.
         clk1_en <= (sel == 0);
         clk2_en <= (sel == 1);
         prev_sel_reg <= sel_reg;
         sel_reg <= sel;
      end
   end

   // clk2 domain: additional control for enabling clk2 on rising edge when switching from clk1 to clk2,
   // and disabling clk2 on rising edge when switching from clk1 to clk2? Actually, we want:
   // When switching from 0 to 1:
   //   - clk1 is disabled on clk1's rising edge (handled in clk1 domain).
   //   - clk2 is enabled on clk2's rising edge.
   // When switching from 1 to 0:
   //   - clk2 is disabled on clk2's rising edge.
   //   - clk1 is enabled on clk1's rising edge (handled in clk1 domain).
   // So in clk2 domain, we only need to handle enabling for 0->1 and disabling for 1->0.
   logic sel_d, prev_sel_d; // registered sel in clk2 domain

   always_ff @(posedge clk2 or negedge rst_n) begin
      if (!rst_n) begin
         sel_d <= 0;
         prev_sel_d <= 0;
         clk2_en <= 0;
      end else begin
         // Sample sel in clk2 domain
         prev_sel_d <= sel_d;
         sel_d <= sel;
         // For transition from 0 to 1: enable clk2 on first rising edge of clk2
         if (prev_sel_d == 0 && sel_d == 1) begin
            clk2_en <= 1;
         end
         // For transition from 1 to 0: disable clk2 on first rising edge of clk2
         if (prev_sel_d == 1 && sel_d == 0) begin
            clk2_en <= 0;
         end
      end
   end

   // Generate clkout using multiplexer controlled by enable signals
   always_comb begin
      if (!rst_n)
         clkout = 0;
      else if (clk1_en)
         clkout = clk1;
      else if (clk2_en)
         clkout = clk2;
      else
         clkout = 0;
   end

endmodule