module glitch_free_mux (
    input  wire         clk1,
    input  wire         clk2,
    input  wire         sel,
    input  wire         rst_n,
    output wire         clkout
);

  // Internal registers to control which clock is enabled.
  reg         clk1_en;
  reg         clk2_en;

  // Cross-domain coordination signals:
  // enable_clk2: asserted in the clk1 domain to request that clk2 be enabled.
  // clk2_disabled: asserted in the clk2 domain to signal that clk2 has been disabled,
  //               so that the clk1 domain can re-enable clk1.
  reg         enable_clk2;
  reg         clk2_disabled;

  // Registers to synchronize and detect transitions of the sel signal in each clock domain.
  // In the clk1 domain:
  reg         sel_sync1;
  reg         sel_prev1;
  // In the clk2 domain:
  reg         sel_sync2;
  reg         sel_prev2;

  // ------------------------------------------------------------------
  // clk1 domain: Manage clk1_en and coordinate switching.
  // When sel transitions from 0 to 1 (rising edge), disable clk1 and request
  // that clk2 be enabled. When clk2 is later disabled (via clk2_disabled),
  // re-enable clk1.
  // ------------------------------------------------------------------
  always @(posedge clk1 or negedge rst_n) begin
    if (!rst_n) begin
      clk1_en       <= 1'b0;
      sel_sync1     <= 1'b0;
      sel_prev1     <= 1'b0;
      clk2_disabled <= 1'b0;
    end else begin
      // Update registers for edge detection.
      sel_prev1 <= sel_sync1;
      sel_sync1 <= sel;
      
      // Detect rising edge of sel (0 -> 1): disable clk1 and request clk2 enable.
      if (sel_prev1 == 1'b0 && sel_sync1 == 1'b1) begin
        clk1_en       <= 1'b0;
        enable_clk2   <= 1'b1;
      end
      // If clk2 has been disabled (detected in the clk2 domain), then re-enable clk1.
      else if (clk2_disabled) begin
        clk1_en       <= 1'b1;
        clk2_disabled <= 1'b0;
      end
      // Default: when no transition is detected, set clk1_en based on sel.
      // When sel is 0, clk1 should be enabled; when sel is 1, clk1 remains disabled.
      else begin
        clk1_en <= (sel_sync1 == 1'b0) ? 1'b1 : 1'b0;
      end
    end
  end

  // ------------------------------------------------------------------
  // clk2 domain: Manage clk2_en and coordinate switching.
  // When a request from the clk1 domain (enable_clk2) is received, enable clk2.
  // When sel transitions from 1 to 0 (falling edge) in this domain, disable clk2
  // and assert clk2_disabled to signal the clk1 domain to re-enable clk1.
  // ------------------------------------------------------------------
  always @(posedge clk2 or negedge rst_n) begin
    if (!rst_n) begin
      clk2_en      <= 1'b0;
      sel_sync2    <= 1'b0;
      sel_prev2    <= 1'b0;
      // Note: enable_clk2 is driven by the clk1 domain.
    end else begin
      // Update registers for edge detection.
      sel_prev2 <= sel_sync2;
      sel_sync2 <= sel;
      
      // If a request to enable clk2 is received, then enable clk2.
      if (enable_clk2) begin
        clk2_en      <= 1'b1;
        enable_clk2  <= 1'b0;
      end
      // Detect falling edge of sel (1 -> 0): disable clk2 and signal clk1 domain.
      else if (sel_prev2 == 1'b1 && sel_sync2 == 1'b0) begin
        clk2_en      <= 1'b0;
        clk2_disabled<= 1'b1;
      end
      // Default: when no transition is detected, set clk2_en based on sel.
      // When sel is 1, clk2 should be enabled; when sel is 0, clk2 remains disabled.
      else begin
        clk2_en <= (sel_sync2 == 1'b1) ? 1'b1 : 1'b0;
      end
    end
  end

  // ------------------------------------------------------------------
  // Output clock generation:
  // The output clock (clkout) is generated by ORing the two input clocks,
  // each gated by its respective enable signal. This ensures that only one
  // clock is active at any time, providing glitch-free switching.
  // ------------------------------------------------------------------
  assign clkout = (clk1 & clk1_en) | (clk2 & clk2_en);

endmodule