module dig_stopwatch #(
    parameter CLK_FREQ = 50000000  // Default clock frequency is 50 MHz
)(
    input wire clk,                // Input clock (parameterized frequency)
    input wire reset,              // Reset signal
    input wire start_stop,         // Start/Stop control
    input wire load,               // Asynchronous load signal for countdown timer
    input [4:0] load_hours,        // Hours to load (valid: 0-23)
    input [5:0] load_minutes,      // Minutes to load (valid: 0-59)
    input [5:0] load_seconds,      // Seconds to load (valid: 0-59)
    output reg [5:0] seconds,      // Seconds counter (0-59)
    output reg [5:0] minutes,      // Minutes counter (0-59)
    output reg [4:0] hour          // Hour counter (0-23)
);

    // Calculate max counter value based on CLK_FREQ
    localparam COUNTER_MAX = CLK_FREQ - 1;
    // Clock divider counter width based on CLK_FREQ
    reg [$clog2(COUNTER_MAX):0] counter;
    // One-second pulse signal generated by the clock divider
    reg one_sec_pulse;

    // Clock divider: Generates a 1 Hz pulse when start_stop is asserted.
    // When start_stop is deasserted, the counter is paused (retaining progress).
    always @(posedge clk or posedge reset) begin
        if (reset) begin
            counter <= 0;
            one_sec_pulse <= 0;
        end else begin
            if (start_stop) begin
                if (counter == COUNTER_MAX) begin
                    counter <= 0;
                    one_sec_pulse <= 1'b1;  // Generate a one-second pulse
                end else begin
                    counter <= counter + 1;
                    one_sec_pulse <= 1'b0;
                end
            end else begin
                // Pause: do not update counter; retain current progress
                one_sec_pulse <= 1'b0;
            end
        end
    end

    // Countdown logic: Updates the timer once per second.
    // Priority: reset > load > countdown.
    always @(posedge clk or posedge reset) begin
        if (reset) begin
            // Reinitialize timer to 00:00:00 on reset
            seconds <= 6'b0;
            minutes <= 6'b0;
            hour    <= 5'b0;
        end else if (load) begin
            // Load new values with range checking.
            // Clamp values to maximum valid if exceeded.
            hour    <= (load_hours  > 23) ? 23 : load_hours;
            minutes <= (load_minutes > 59) ? 59 : load_minutes;
            seconds <= (load_seconds > 59) ? 59 : load_seconds;
        end else if (start_stop && one_sec_pulse) begin
            // Only decrement if not already at 00:00:00.
            if ((hour == 0) && (minutes == 0) && (seconds == 0)) begin
                // Timer already at 00:00:00; hold state.
            end else begin
                if (seconds == 0) begin
                    if (minutes == 0) begin
                        if (hour == 0) begin
                            // Underflow: already at 00:00:00; do nothing.
                        end else begin
                            // Transition from, e.g., 1:00:00 -> 0:59:59
                            seconds <= 59;
                            minutes <= 59;
                            hour    <= hour - 1;
                        end
                    end else begin
                        // Transition from, e.g., 0:01:00 -> 0:00:59
                        seconds <= 59;
                        minutes <= minutes - 1;
                    end
                end else begin
                    // Normal decrement of seconds.
                    seconds <= seconds - 1;
                end
            end
        end
        // If start_stop is deasserted, the timer holds its current values.
    end

endmodule