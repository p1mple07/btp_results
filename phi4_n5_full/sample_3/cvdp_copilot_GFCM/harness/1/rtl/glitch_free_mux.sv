// Glitch-Free Clock Switching (MUX) RTL Design
//
// This module switches between two synchronous clock sources (clk1 and clk2)
// based on the select input (sel). When sel = 0, clkout follows clk1; when sel = 1,
// clkout follows clk2. The switching is performed glitch‐free by gating each clock
// with an enable signal. The transitions occur on the appropriate rising edges:
// • For a 0 → 1 transition (sel goes from 0 to 1), clk1 is disabled on the first rising
//   edge of clk1 after the change, and clk2 is enabled on the first rising edge of clk2.
// • For a 1 → 0 transition, clk2 is disabled on the first rising edge of clk2 after the change,
//   and clk1 is re-enabled on the first rising edge of clk1 after clk2 is disabled.
// The asynchronous active-low reset (rst_n) forces clkout low.
//
// NOTE: This implementation assumes that sel is synchronous to clk1.
// If sel is synchronous to clk2 instead, the detection logic must be mirrored.
//

module glitch_free_mux (
    input  logic         clk1,
    input  logic         clk2,
    input  logic         sel,
    input  logic         rst_n,
    output logic         clkout
);

  // Enable signals for each clock
  logic clk1_en;
  logic clk2_en;

  // Flags to indicate a pending switch request
  // switch_to_clk2: set when a 0->1 transition is detected (request to enable clk2)
  // switch_to_clk1: set when a 1->0 transition is detected (request to disable clk2)
  logic switch_to_clk2;
  logic switch_to_clk1;

  // Register to hold the previous value of sel (sampled in clk1 domain)
  logic sel_reg;

