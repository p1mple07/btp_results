module cdc_pulse_synchronizer #(
    parameter NUM_CHANNELS = 1
) (
    input  logic src_clock,       // Source Clock Domain
    input  logic des_clock,       // Destination Clock Domain
    input  logic rst_in,          // Asynchronous Active-High Reset
    input  logic [NUM_CHANNELS-1:0] src_pulse, // Vector of single-cycle pulses in source domain
    output logic [NUM_CHANNELS-1:0] des_pulse  // Vector of synchronized pulses in destination domain
);

    //-------------------------------------------------------------------------
    // Reset Synchronizers
    //-------------------------------------------------------------------------
    // Source domain reset synchronizer
    logic rst_src_synca, rst_src_syncb, rst_src_sync;
    always_ff @(posedge src_clock or posedge rst_in) begin
        if (rst_in) begin
            rst_src_synca <= 1'b1;
            rst_src_syncb <= 1'b1;
        end else begin
            rst_src_synca <= 1'b0;
            rst_src_syncb <= rst_src_synca;
        end
    end
    assign rst_src_sync = rst_src_syncb;

    // Destination domain reset synchronizer
    logic rst_des_synca, rst_des_syncb, rst_des_sync;
    always_ff @(posedge des_clock or posedge rst_in) begin
        if (rst_in) begin
            rst_des_synca <= 1'b1;
            rst_des_syncb <= 1'b1;
        end else begin
            rst_des_synca <= 1'b0;
            rst_des_syncb <= rst_des_synca;
        end
    end
    assign rst_des_sync = rst_des_syncb;

    //-------------------------------------------------------------------------
    // Source Domain: Toggle Generation for Each Channel
    //-------------------------------------------------------------------------
    // For each channel, convert the single-cycle pulse into a toggle signal.
    logic pls_toggle_src [NUM_CHANNELS-1:0];
    integer i;
    always_ff @(posedge src_clock or posedge rst_src_sync) begin
        if (rst_src_sync) begin
            for (i = 0; i < NUM_CHANNELS; i = i + 1) begin
                pls_toggle_src[i] <= 1'b0;
            end
        end else begin
            for (i = 0; i < NUM_CHANNELS; i = i + 1) begin
                if (src_pulse[i])
                    pls_toggle_src[i] <= ~pls_toggle_src[i];
            end
        end
    end

    //-------------------------------------------------------------------------
    // Destination Domain: CDC Synchronizers for Each Channel
    //-------------------------------------------------------------------------
    // The toggle signal is transferred through three stages.
    logic pls_toggle_des_sync0 [NUM_CHANNELS-1:0];
    logic pls_toggle_des_sync1 [NUM_CHANNELS-1:0];
    logic pls_toggle_des_sync2 [NUM_CHANNELS-1:0];

    // First stage: Two flip-flops in series
    always_ff @(posedge des_clock or posedge rst_des_sync) begin
        if (rst_des_sync) begin
            for (i = 0; i < NUM_CHANNELS; i = i + 1) begin
                pls_toggle_des_sync0[i] <= 1'b0;
                pls_toggle_des_sync1[i] <= 1'b0;
            end
        end else begin
            for (i = 0; i < NUM_CHANNELS; i = i + 1) begin
                pls_toggle_des_sync0[i] <= pls_toggle_src[i];
                pls_toggle_des_sync1[i] <= pls_toggle_des_sync0[i];
            end
        end
    end

    // Second stage: One flip-flop
    always_ff @(posedge des_clock or posedge rst_des_sync) begin
        if (rst_des_sync) begin
            for (i = 0; i < NUM_CHANNELS; i = i + 1) begin
                pls_toggle_des_sync2[i] <= 1'b0;
            end
        end else begin
            for (i = 0; i < NUM_CHANNELS; i = i + 1) begin
                pls_toggle_des_sync2[i] <= pls_toggle_des_sync1[i];
            end
        end
    end

    //-------------------------------------------------------------------------
    // Pulse Detection: Generate a pulse when the toggle changes
    //-------------------------------------------------------------------------
    // For each channel, the pulse is generated by detecting a rising edge
    // in the synchronized toggle signal (XOR of two stages).
    genvar j;
    generate
        for (j = 0; j < NUM_CHANNELS; j = j + 1) begin : gen_pulse
            assign des_pulse[j] = pls_toggle_des_sync2[j] ^ pls_toggle_des_sync1[j];
        end
    endgenerate

endmodule