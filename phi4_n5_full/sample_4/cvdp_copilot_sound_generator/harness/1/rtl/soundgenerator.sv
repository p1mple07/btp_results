module soundgenerator #(
	parameter CLOCK_HZ = 10_000_000
)(
	input  wire         clk,                                         // 10 MHz clock
	input  wire         nrst,                                        // active low asynchronous reset
	input  wire         start,                                       // start sound generation
	input  wire         finish,                                      // end operation signal
	input  wire [15:0]  sond_dur_ms_i,                               // duration in milliseconds
	input  wire [15:0]  half_period_us_i,                            // half period in microseconds
	output wire         soundwave_o,                                 // generated square wave output
	output wire         busy,                                        // system busy signal
	output wire         done                                          // operation complete signal
);

	// Internal registers
	reg  [15:0] duration_counter;   // ms counter for duration
	reg  [15:0] half_period_counter; // counter for half period (microseconds)
	reg         sound_reg;          // holds the current state of the square wave
	reg         busy_reg;           // internal busy flag (active when duration_counter != 0)
	reg         done_reg;           // pulse when busy goes from high to low or finish asserted
	reg         prev_busy;          // used to detect transition from busy high to low

	// Wires for strobe pulses generated by strob_gen instances
	wire tickmillis;  // pulse every 1 ms (1000 us)
	wire tickmicro;   // pulse every 1 us

	//---------------------------------------------------------------------
	// Instantiate strob_gen for 1 ms pulse (TickMilli)
	// For 1 ms, PERIOD_US = 1000 (since 1 ms = 1000 us)
	//---------------------------------------------------------------------
	strob_gen #(
		.CLOCK_HZ( CLOCK_HZ ),
		.PERIOD_US( 1000 )
	) tickmillis_gen (
		.clk     ( clk ),
		.nrst    ( nrst ),
		.enable  ( busy_reg ),  // generate strobe only when busy
		.strobe_o( tickmillis )
	);

	//---------------------------------------------------------------------
	// Instantiate strob_gen for 1 us pulse (TickMicro)
	// For 1 us, PERIOD_US = 1
	//---------------------------------------------------------------------
	strob_gen #(
		.CLOCK_HZ( CLOCK_HZ ),
		.PERIOD_US( 1 )
	) tickmicro_gen (
		.clk     ( clk ),
		.nrst    ( nrst ),
		.enable  ( busy_reg ),  // generate strobe only when busy
		.strobe_o( tickmicro )
	);

	//---------------------------------------------------------------------
	// Main sequential process for sound generator
	//---------------------------------------------------------------------
	always @(posedge clk or negedge nrst) begin
		if (!nrst) begin
			// Synchronous reset: clear all registers
			duration_counter     <= 16'd0;
			half_period_counter  <= 16'd0;
			sound_reg            <= 1'b0;
			busy_reg             <= 1'b0;
			done_reg             <= 1'b0;
			prev_busy            <= 1'b0;
		end else begin
			// If finish is asserted, immediately end operation.
			if (finish) begin
				duration_counter     <= 16'd0;
				half_period_counter  <= 16'd0;
				sound_reg            <= 1'b0;
				done_reg             <= 1'b1;  // signal completion
			end else begin
				// On start, load the duration and half period counters.
				if (start) begin
					duration_counter     <= sond_dur_ms_i;
					half_period_counter  <= half_period_us_i;
					sound_reg            <= 1'b0;
				end

				// Update the duration counter on every 1 ms tick
				if (tickmillis && (duration_counter != 16'd0)) begin
					duration_counter <= duration_counter - 16'd1;
				end

				// Update the half period counter on every 1 us tick
				if (tickmicro && (duration_counter != 16'd0)) begin
					if (half_period_counter != 16'd0)
						half_period_counter <= half_period_counter - 16'd1;
					else begin
						half_period_counter <= half_period_us_i;
						sound_reg           <= ~sound_reg;  // toggle square wave
					end
				end
			end

			// Set busy flag based on duration counter (active when non-zero)
			busy_reg <= (duration_counter != 16'd0);

			// Detect transition from busy high to low to assert done.
			// Also, if finish is asserted, done is high.
			if (finish)
				done_reg <= 1'b1;
			else if (prev_busy && !busy_reg)
				done_reg <= 1'b1;
			else
				done_reg <= 1'b0;

			// Save current busy state for next cycle transition detection
			prev_busy <= busy_reg;
		end
	end

	// Drive outputs: soundwave_o is active only when busy; otherwise 0.
	assign soundwave_o = busy_reg ? sound_reg : 1'b0;
	assign busy         = busy_reg;
	assign done         = done_reg;

endmodule

/////////////////////////////////////////////////////////////////////////
// Module: strob_gen
//
// This module generates a periodic strobe signal with a pulse width of one
// clock cycle. The strobe frequency is configurable via the PERIOD_US parameter.
// When the enable signal is low, the counter is reloaded and no strobe pulse is generated.
/////////////////////////////////////////////////////////////////////////
module strob_gen #(
	parameter CLOCK_HZ  = 10_000_000,
	parameter PERIOD_US = 100
)(
	input  wire clk,
	input  wire nrst,
	input  wire enable,
	output reg  strobe_o
);

	// Calculate the delay value (in clock cycles) for the given period.
	// DELAY = (CLOCK_HZ / 1_000_000) * PERIOD_US.
	// For CLOCK_HZ = 10 MHz and PERIOD_US = 100, DELAY = 10 * 100 = 1000.
	localparam DELAY = CLOCK_HZ / 1_000_000 * PERIOD_US;

	reg [31:0] counter;

	always @(posedge clk or negedge nrst) begin
		if (!nrst) begin
			counter    <= DELAY;
			strobe_o   <= 1'b0;
		end else begin
			// When enable is low, reload the counter and suppress strobe.
			if (!enable) begin
				counter    <= DELAY;
				strobe_o   <= 1'b0;
			end else begin
				if (counter == 32'd0) begin
					strobe_o <= 1'b1;  // pulse high for one clock cycle
					counter  <= DELAY; // reload counter
				end else begin
					strobe_o <= 1'b0;
					counter  <= counter - 1;
				end
			end
		end
	end

endmodule