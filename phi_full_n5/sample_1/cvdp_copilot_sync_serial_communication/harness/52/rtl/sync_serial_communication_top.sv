`timescale 1ns / 1ps
module sync_serial_communication_tx_rx(
    input clk,                     // Clock signal
    input reset_n,                 // Active low reset signal
    input [2:0] sel,               // Selection signal for TX block
    input [63:0] data_in,          // 64-bit data input for TX block
    output [63:0] data_out,        // 64-bit data output from RX block
    output done                    // Done signal indicating completion
);

// Internal signals
wire tx_out;                       // Serial data output from TX block
wire tx_done;                      // Done signal from TX block
wire serial_clk_in;                // Serial clock generated by TX block

// Instantiate the TX block
tx_block uut_tx_block (
    .clk(clk),                     // Clock input to TX block
    .reset_n(reset_n),             // Reset input to TX block
    .data_in(data_in),             // 64-bit data input to TX block
    .sel(sel),                     // Selection signal to TX block
    .serial_out(tx_out),           // Serial data output from TX block
    .done(tx_done),                // Done signal from TX block
    .serial_clk(serial_clk_in)     // Serial clock output from TX block
);

// Instantiate the RX block
rx_block uut_rx_block (
    .clk(clk),                     // Clock input to RX block
    .serial_clk(serial_clk_in),    // Serial clock input to RX block
    .reset_n(reset_n),             // Reset input to RX block
    .sel(sel),                     // Selection signal to RX block
    .data_in(tx_out),              // Serial data input to RX block
    .data_out(data_out),           // 64-bit data output from RX block
    .done(done)                    // Done signal from RX block
);

// Combined sequential and combinational logic
always @(posedge clk or negedge reset_n) begin
    if (!reset_n) begin
        // Reset all values on active-low reset
        tx_out <= 64'h0;
        tx_done <= 1'b0;
        data_out <= 64'h0;
        bit_count <= 8'd0;
        reg_count <= 7'h0;
        temp_reg_count <= 7'h0;
    end
    else begin
        case (sel)
            3'b000:begin
                data_reg <= 64'h0;
                bit_count <= 7'd0;
                reg_count <= 7'd0;
                serial_clk_in <= #3'(~clk); // Generate clock with 3-cycle latency
            end
            3'b001:begin
                data_reg <= {56'h0, data_in[7:0]};
                bit_count <= 7'd7;
                reg_count <= 7'd7;
                serial_clk_in <= #3'(~clk);
            end
            3'b010:begin
                data_reg <= {48'h0, data_in[15:0]};
                bit_count <= 7'd15;
                reg_count <= 7'd15;
                serial_clk_in <= #3'(~clk);
            end
            3'b011:begin
                data_reg <= {32'h0, data_in[31:0]};
                bit_count <= 7'd31;
                reg_count <= 7'd31;
                serial_clk_in <= #3'(~clk);
            end
            3'b100:begin
                data_reg <= data_in[63:0];
                bit_count <= 7'd63;
                reg_count <= 7'd63;
                serial_clk_in <= #3'(~clk);
            end
            default: begin
                data_reg <= 64'h0;
                bit_count <= 7'd0;
                reg_count <= 7'd0;
                serial_clk_in <= #3'(~clk);
            end
        end
        
        // Update reg_count with bit_count
        reg_count <= bit_count;

        // Shift data_reg and generate serial_clk
        if (bit_count > 7'd0) begin
            data_reg <= data_reg >> 1;
            bit_count <= bit_count - 1'd1;
            serial_clk_in <= #3'(~clk);
        end else begin
            serial_clk_in <= #3'(~clk);
        end
    end
end

// Generate serial_clk using a shift register
always @(posedge clk or negedge reset_n) begin
    if (!reset_n) begin
        temp_reg_count <= 7'h0;
    end else begin
        temp_reg_count <= reg_count;
    end
end

// Use the shift register to generate serial_clk
assign serial_clk = temp_reg_count[6:0];

endmodule
