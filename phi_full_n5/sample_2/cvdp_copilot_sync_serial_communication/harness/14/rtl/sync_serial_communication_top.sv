module sync_serial_communication_tx_rx(
    input clk,                     // Clock signal
    input reset_n,                 // Active low reset signal
    input [2:0] sel,               // Selection signal for TX block
    input [63:0] data_in,          // 64-bit data input for TX block
    output [63:0] data_out,        // 64-bit data output from RX block
    output done,                    // Done signal indicating completion
    output parity,                // Parity bit for data integrity check
    output parity_error         // Parity error signal
);

// Internal signals
wire tx_out;                       // Serial data output from TX block
wire tx_done;                      // Done signal from TX block
wire serial_clk_in;                // Serial clock generated by TX block
wire parity_in;                   // Parity bit received by RX block

// Instantiate the TX block
tx_block uut_tx_block (
    .clk(clk),                     // Clock input to TX block
    .reset_n(reset_n),             // Reset input to TX block
    .data_in(data_in),             // 64-bit data input to TX block
    .sel(sel),                     // Selection signal to TX block
    .serial_out(tx_out),           // Serial data output from TX block
    .done(tx_done),                // Done signal from TX block
    .serial_clk(serial_clk_in)     // Serial clock output from TX block
    .parity(parity)              // Parity bit output from TX block
);

// Instantiate the RX block
rx_block uut_rx_block (
    .clk(clk),                     // Clock input to RX block
    .serial_clk(serial_clk_in),    // Serial clock input to RX block
    .reset_n(reset_n),             // Reset input to RX block
    .data_in(tx_out),              // Serial data input to RX block
    .parity_in(parity_in),         // Parity bit input to RX block
    .done(done),                    // Done signal from RX block
    .parity_error(parity_error),    // Parity error signal from RX block
    .data_out(data_out)           // 64-bit data output from RX block
);

// Modifications for tx_block
always @(posedge clk or negedge reset_n) begin
    if (!reset_n) begin
        // Reset all values on active-low reset
        data_reg     <= 64'h0;			     	           // Clear the data register
        bit_count    <= 7'h0;			     	           // Reset bit count to zero
        reg_count    <= 7'h0;		             	       // Reset reg_count to zero
        parity       <= 64'h0;                   // Clear parity bit
    end else begin
        if (done == 1'b1) begin
            // Case block to determine the width of data to transmit based on the 'sel' input
            case (sel)
                3'b000: begin
                    data_reg  <= 64'h0;             	   // No data selected
                    bit_count <= 7'd0;                     // No bits to transmit
                    parity     <= 64'h0;                   // No parity bit
                end
                3'b001: begin
                    data_reg  <= {56'h0, data_in[7:0]};    // Transmit lowest 8 bits
                    bit_count <= 7'd7;              	   // 8 bits to transmit
                    parity     <= data_in[7:0] ^ {49'h0, 49'h0}; // Calculate and transmit parity
                end
                3'b010: begin
                    data_reg  <= {48'h0, data_in[15:0]};   // Transmit lowest 16 bits
                    bit_count <= 7'd15;             	   // 16 bits to transmit
                    parity     <= data_in[15:0] ^ {38'h0, 38'h0}; // Calculate and transmit parity
                end
                3'b011: begin
                    data_reg  <= {32'h0, data_in[31:0]};   // Transmit lowest 32 bits
                    bit_count <= 7'd31;             	   // 32 bits to transmit
                    parity     <= data_in[31:0] ^ {16'h0, 16'h0}; // Calculate and transmit parity
                end
                3'b100: begin
                    data_reg  <= data_in[63:0];     	   // Transmit all 64 bits
                    bit_count <= 7'd63;             	   // 64 bits to transmit
                    parity     <= data_in[63:0] ^ {63'h0, 63'h0}; // Calculate and transmit parity
                end
                default: begin
                    data_reg  <= 64'h0;             	   // Default case: no data
                    bit_count <= 7'h0;              	   // No bits to transmit
                    parity     <= 64'h0;                   // No parity bit
                end
            endcase
            
        end else if (bit_count > 7'h0) begin
            data_reg   <= data_reg >> 1;                   // Shift the data to the right
            bit_count  <= bit_count - 1'b1;                // Decrement the bit count
            parity     <= parity ^ data_reg[bit_count];     // Update parity bit
        end
        reg_count <= bit_count;                            // Update reg_count for serial clock control
    end
end

// Modifications for rx_block
always @(posedge clk or negedge reset_n) begin
    if (!reset_n) begin
        // Reset all registers on reset_n (active low)
        data_reg <= 64'h0;					           // Clear the data register
        bit_count <= 8'd0;					           // Clear the bit counter
        parity_error <= 1'b0;                           // Clear parity error
    end
    else begin
        bit_count <= bit_count;					           // Maintain the bit counter if not done
        parity_error <= (parity_in != (data_reg[bit_count] ^ reg_count)); // Check parity error
    end
end

// Sequential logic to drive the serial_out signal based on reg_count and bit_count
always@(posedge clk or negedge reset_n) begin
    if(!reset_n) begin
        serial_out <= 1'b0;				                  // Reset serial output
    end
    else if(reg_count > 7'h0 || bit_count > 7'h0) begin
	   serial_out <= data_reg[0];			              // Output the least significant bit of data_reg
    end
end

// Set the done signal when transmission is complete (bit_count reaches zero)
always@(posedge clk or negedge reset_n) begin
    if(!reset_n) begin
        done <= 1'b0;					                  // Reset done output
    end 
    else begin
     if(bit_count == 7'h0) begin
	    done <= 1'b1; 			                          // Set done signal high when transmission is done
     end
     else begin 
	    done <= 1'b0;					                  // Clear done signal during transmission
     end
end

