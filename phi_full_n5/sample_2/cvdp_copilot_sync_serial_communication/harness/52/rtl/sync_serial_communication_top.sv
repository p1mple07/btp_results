`timescale 1ns / 1ps
module sync_serial_communication_tx_rx(
    input clk,                     // Clock signal
    input reset_n,                 // Active low reset signal
    input [2:0] sel,               // Selection signal for TX block
    input [63:0] data_in,          // 64-bit data input for TX block
    output reg [63:0] data_out,    // 64-bit data output from RX block
    output reg done                // Done signal indicating completion
);

// Internal signals
wire tx_reg, rx_reg;                 // Register for internal data transfer
wire tx_done, rx_done;                // Done signals for TX and RX blocks
wire serial_clk_in;                  // Serial clock generated by TX block

// Instantiate the TX and RX blocks
tx_block uut_tx_block (
    .clk(clk),                       // Clock input to TX block
    .reset_n(reset_n),               // Reset input to TX block
    .data_in(data_in),               // 64-bit data input to TX block
    .sel(sel),                       // Selection signal to TX block
    .data_out(tx_reg),               // 64-bit data output from TX block
    .done(tx_done),                  // Done signal from TX block
    .serial_clk(serial_clk_in)       // Serial clock output from TX block
);

rx_block uut_rx_block (
    .clk(clk),                       // Clock input to RX block
    .reset_n(reset_n),               // Reset input to RX block
    .data_in(rx_reg),                // 64-bit data input to RX block
    .sel(sel),                       // Selection signal to RX block
    .data_out(data_out),            // 64-bit data output from RX block
    .done(rx_done)                  // Done signal from RX block
);

// Combined always block for TX and RX
always @(posedge clk or negedge reset_n) begin
    if (!reset_n) begin
        tx_reg <= 64'h0;
        rx_reg <= 64'h0;
        bit_count <= 7'h0;
        reg_count <= 7'h0;
        temp_reg_count <= 7'h0;
        done <= 1'b0;
    end
    else begin
        case(sel)
            3'b000: begin
                tx_reg <= 64'h0;
                rx_reg <= 64'h0;
                bit_count <= 7'd0;
                reg_count <= 7'd0;
            end
            3'b001: begin
                tx_reg <= {56'h0, data_in[7:0]};
                rx_reg <= {56'h0, data_in[7:0]};
                bit_count <= 7'd7;
                reg_count <= 7'd7;
            end
            3'b010: begin
                tx_reg <= {48'h0, data_in[15:0]};
                rx_reg <= {48'h0, data_in[15:0]};
                bit_count <= 7'd15;
                reg_count <= 7'd15;
            end
            3'b011: begin
                tx_reg <= {32'h0, data_in[31:0]};
                rx_reg <= {32'h0, data_in[31:0]};
                bit_count <= 7'd31;
                reg_count <= 7'd31;
            end
            3'b100: begin
                tx_reg <= data_in;
                rx_reg <= data_in;
                bit_count <= 7'd63;
                reg_count <= 7'd63;
            end
            default: begin
                tx_reg <= 64'h0;
                rx_reg <= 64'h0;
                bit_count <= 7'h0;
                reg_count <= 7'h0;
            end
        end
    end

    // Calculate serial clock with added 3-cycle latency
    case(reg_count)
        7'd0: serial_clk_in = 1'b0;
        7'd1: serial_clk_in = clk && temp_reg_count !== 7'd0;
        7'd2: serial_clk_in = clk && temp_reg_count !== 7'd1;
        7'd3: serial_clk_in = clk;
        default: begin
            serial_clk_in = clk && temp_reg_count !== 7'd2;
        end
    end

    // Calculate done signals with added 3-cycle latency
    case(bit_count)
        7'd0: done = 1'b0;
        7'd1: done = 1'b0;
        7'd2: done = 1'b0;
        7'd3: done = 1'b0;
        7'd4: done = 1'b0;
        7'd5: done = 1'b0;
        7'd6: done = 1'b0;
        7'd7: done = 1'b0;
        7'd8: done = 1'b1;
        default: begin
            done = 1'b0;
        end
    end
end

endmodule

///// tx_block
module tx_block(
    input clk,               // Clock input
    input reset_n,           // Active-low reset input
    input [63:0] data_in,    // 64-bit parallel data input
    input [2:0] sel,         // Selection input to choose data width
    output reg serial_out,   // Serial data output
    output reg done,         // Done signal indicating completion of transmission
    output reg serial_clk        // Clock for serial data transmission
);

// Internal registers
reg [63:0] data_reg;         // Register to hold the data being transmitted
reg [6:0] bit_count;         // Counter to track number of bits to transmit
reg [6:0] reg_count;         // Register for counting bits for serial clock control
reg [6:0] temp_reg_count;    // Temporary register to track reg_count

// Sequential block for state control and data selection
always @(posedge clk or negedge reset_n) begin
    if (!reset_n) begin
        data_reg     <= 64'h0;
        bit_count    <= 7'd0;
        reg_count    <= 7'h0;
        temp_reg_count <= 7'h0;
    end else begin
        bit_count <= bit_count + 1'b1;
        reg_count <= reg_count + 1'b1;
        temp_reg_count <= temp_reg_count + 1'b1;
    end
end

// Generate serial clock based on reg_count
always @(posedge clk or negedge reset_n) begin
    if (!reset_n) begin
        temp_reg_count <= 7'h0;
    end
    else begin
        temp_reg_count <= reg_count;
    end
end

// Sequential logic to drive the serial_out signal based on reg_count and bit_count
always @(posedge clk or negedge reset_n) begin
    if (!reset_n) begin
        serial_out <= 1'b0;
    end
    else if(reg_count > 7'h0 || bit_count > 7'h0) begin
        serial_out <= data_reg[bit_count - 1];
    end
end

// Set the done signal when transmission is complete (bit_count reaches zero)
always @(posedge clk or negedge reset_n) begin
    if (!reset_n) begin
        done <= 1'b0;
    end 
    else begin
        if(bit_count == 7'h0) begin
            done <= 1'b1;
        end
        else begin
            done <= 1'b0;
        end
    end
end

endmodule

///// rx_block
module rx_block(
    input wire clk,  			    // clock input
    input wire reset_n,			    // Active-low reset
    input wire data_in,			    // Serial input data
    input wire serial_clk,		    // Clock signal for serial data
    input wire [2:0] sel,           // Selection output to choose data width
    output reg [63:0] data_out  	// Parallel output data after serial operation
)

// Internal registers
reg [63:0] data_reg;		    // Register to hold incoming serial data
reg [7:0] bit_count;		    // Counter for the number of bits received
reg [7:0] count;			    // General purpose counter to sync with bit_count

// Sequential logic block to capture serial data on the rising edge of serial_clk
always @(posedge serial_clk or negedge reset_n) begin
    if (!reset_n) begin
        data_reg <= 64'h0;
        bit_count <= 8'd0;
    end
    else if(done == 1'b1) begin
        data_reg <= 64'h0;
        bit_count <= 8'b0;
    end
    else begin
        bit_count <= bit_count;
        data_reg[bit_count] <= data_in;
    end
end

// Sequential logic block to monitor the completion of data reception and update outputs
always @(posedge clk or negedge reset_n) begin
    if (!reset_n) begin
        count <= 8'd0;
        done <= 1'b0;
        data_out <= 64'h0;
    end
    else if(count >= bit_count && count != 8'd0) begin
        done <= 1'b1;
        case(sel)
            3'b000: data_out <= 64'h0;
            3'b001: data_out <= data_reg;
            3'b010: data_out <= data_reg;
            3'b011: data_out <= data_reg;
            3'b100: data_out <= data_reg;
            default: data_out <= 64'h0;
        end
    end
    else if(bit_count > 8'd0) begin
        count <= count + 1'b1;
        done <= 1'b0;
    end
    else begin
        count <= 8'd0;
        done <= 1'b0;
    end
end

endmodule
