// Modified sync_serial_communication_tx_rx module with parity bit feature

module sync_serial_communication_tx_rx(
    input clk,                     // Clock signal
    input reset_n,                 // Active-low reset signal
    input [2:0] sel,               // Selection signal for TX block
    input [63:0] data_in,          // 64-bit data input for TX block
    output reg [63:0] data_out,        // 64-bit data output from RX block
    output reg done,                    // Done signal indicating completion
    output reg parity,              // Parity bit for transmission
    output reg parity_error        // Parity error indicator
);

// Internal signals
wire tx_out;                       // Serial data output from TX block
wire tx_done;                      // Done signal from TX block
wire serial_clk_in;                // Serial clock generated by TX block
wire parity_in;                    // Parity bit input to RX block

// Instantiate the TX block
tx_block uut_tx_block (
    .clk(clk),                     // Clock input to TX block
    .reset_n(reset_n),             // Reset input to TX block
    .data_in(data_in),             // 64-bit data input to TX block
    .sel(sel),                     // Selection signal to TX block
    .serial_out(tx_out),           // Serial data output from TX block
    .done(tx_done),                // Done signal from TX block
    .serial_clk(serial_clk_in)     // Serial clock output from TX block
    .parity(parity)              // Parity bit output from TX block
);

// Instantiate the RX block
rx_block uut_rx_block (
    .clk(clk),                     // Clock input to RX block
    .serial_clk(serial_clk_in),    // Serial clock input to RX block
    .reset_n(reset_n),             // Reset input to RX block
    .sel(sel),                     // Selection signal to RX block
    .data_in(tx_out),              // Serial data input to RX block
    .data_out(data_out),           // 64-bit data output from RX block
    .parity_in(parity_in),         // Parity bit input to RX block
    .done(done),                    // Done signal from RX block
    .parity_error(parity_error)      // Parity error indicator from RX block
);

endmodule

///// tx_block
module tx_block(
    input clk,               // Clock input
    input reset_n,           // Active-low reset input
    input [63:0] data_in,    // 64-bit parallel data input
    input [2:0] sel,         // Selection input to choose data width
    output reg serial_out,   // Serial data output
    output reg done,         // Done signal indicating completion of transmission
    output reg parity,      // Parity bit for transmission
    output reg serial_clk,    // Clock for serial data transmission
    output reg parity_error  // Parity error indicator
);

// Internal registers
reg [63:0] data_reg;         // Register to hold the data being transmitted
reg [6:0] bit_count;         // Counter to track number of bits to transmit
reg [6:0] reg_count;         // Register for counting bits for serial clock control
reg [6:0] temp_reg_count;    // Temporary register to track reg_count
reg [31:0] parity_bit;       // Register to hold the parity bit

// Sequential block for state control, data selection, and parity computation
always @(posedge clk or negedge reset_n) begin
    if (!reset_n) begin
        // Reset all values on active-low reset
        data_reg     <= 64'h0;			     	           // Clear the data register
        bit_count    <= 7'h0;			     	           // Reset bit count to zero
        reg_count    <= 7'h0;		             	       // Reset reg_count to zero
        parity_bit <= 32'h0;                     // Clear the parity bit
    end else begin
        if (done == 1'b1) begin
	    // Case block to determine the width of data to transmit based on the 'sel' input
            case (sel)
                3'b000: begin
                    data_reg  <= 64'h0;             	   // No data selected
                    bit_count <= 7'd0;                     // No bits to transmit
                    parity_bit <= 32'h0;                   // No parity bit
                end
                3'b001: begin
                    data_reg  <= {56'h0, data_in[7:0]};    // Transmit lowest 8 bits
                    bit_count <= 7'd7;              	   // 8 bits to transmit
                    parity_bit <= data_in[7] ^ {56'h0[7:0]}; // Compute and store parity bit
                end
                3'b010: begin
                    data_reg  <= {48'h0, data_in[15:0]};   // Transmit lowest 16 bits
                    bit_count <= 7'd15;             	   // 16 bits to transmit
                    parity_bit <= data_in[15] ^ {48'h0[15:0]}; // Compute and store parity bit
                end
                3'b011: begin
                    data_reg  <= {32'h0, data_in[31:0]};   // Transmit lowest 32 bits
                    bit_count <= 7'd31;             	   // 32 bits to transmit
                    parity_bit <= data_in[31] ^ {32'h0[31:0]}; // Compute and store parity bit
                end
                3'b100: begin
                    data_reg  <= data_in[63:0];     	   // Transmit all 64 bits
                    bit_count <= 7'd63;             	   // 64 bits to transmit
                    parity_bit <= data_in[63];          // Compute and store parity bit
                end
                default: begin
                    data_reg  <= 64'h0;             	   // Default case: no data
                    bit_count <= 7'h0;              	   // No bits to transmit
                    parity_bit <= 32'h0;                   // No parity bit
                end
            endcase
           
        end else if (bit_count > 7'h0) begin
            data_reg   <= data_reg >> 1;                   // Shift the data to the right
            bit_count  <= bit_count - 1'b1;                // Decrement the bit count
            parity_bit <= parity_bit ^ (data_reg[bit_count-1]; // Update parity bit with the new bit
            
        end
        reg_count <= bit_count;                            // Update reg_count for serial clock control
    end
end

// Generate serial clock based on reg_count
//`ifdef SIMULATION
assign  #1 serial_clk = clk && (temp_reg_count !== 7'd0) ;  // To avoid SImulator issue 
//`else
//assign  serial_clk = clk && (temp_reg_count !== 7'd0) ;  
//`endif

// Register to keep track of reg_count and use for clock gating
always@(posedge clk or negedge reset_n) begin 
    if(!reset_n) begin
        temp_reg_count <= 7'h0;					           // Reset temporary reg_count
    end
    else begin
        temp_reg_count <= reg_count;			           // Update temp_reg_count with current reg_count
    end
end

// Sequential logic to drive the serial_out signal based on reg_count and bit_count
always@(posedge clk or negedge reset_n) begin 
    if(!reset_n) begin
	   serial_out <= 1'b0;				                  // Reset serial output
    end
    else if(reg_count > 7'h0 || bit_count > 7'h0) begin
	   serial_out <= data_reg[0];			              // Output the least significant bit of data_reg
    end
end

// Set the done signal when transmission is complete (bit_count reaches zero)
always@(posedge clk or negedge reset_n) begin
    if(!reset_n) begin
        done <= 1'b0;					                  // Reset done output
    end 
    else begin
     if(bit_count == 7'h0) begin
	    done <= 1'b1; 			                          // Set done signal high when transmission is done
     end
     else begin 
	    done <= 1'b0;					                  // Clear done signal during transmission
     end
end

// Compute parity error
always@(posedge clk or negedge reset_n) begin
    if(!reset_n) begin
        parity_error <= 1'b0;
    end else begin
        case(sel)
            3'b000: begin
                parity_error <= (parity_bit == 32'h0);
            end
            3'b001: begin
                parity_error <= (parity_bit == data_in[7]);
            end
            3'b010: begin
                parity_error <= (parity_bit == data_in[15]);
            end
            3'b011: begin
                parity_error <= (parity_bit == data_in[31]);
            end
            3'b100: begin
                parity_error <= (parity_bit == data_in[63]);
            end
            default: begin
                parity_error <= 1'b0;
            end
        endcase
    end
end

endmodule

///// rx_block
module rx_block(
    input wire clk,  			    // clock input
    input wire reset_n,			    // Active-low reset
    input wire data_in,			    // Serial input data
    input wire serial_clk,		    // Clock signal for serial data
    input wire [2:0] sel,           // Selection output to choose data width
    output reg done,			    // Done signal to indicate data reception complete		
    output reg [63:0] data_out  	// Parallel output data after serial operation
    output reg parity_error;         // Parity error indicator

);

    // Internal registers
    reg [63:0] data_reg;		    // Register to hold incoming serial data
    reg [7:0] bit_count;		    // Counter for the number of bits received	
    reg [7:0] count;			    // General purpose counter to sync with bit_count


    // Sequential logic block to capture serial data on the rising edge of serial_clk
    always @(posedge serial_clk or negedge  reset_n) begin
        if (!reset_n) begin		    // Reset all registers on reset_n (active low)
            data_reg <= 64'h0;		// Clear the data register
            bit_count <= 8'd0;		// Clear the bit counter
        end else begin
            if(done == 1'b1) begin                   // If done signal is asserted, reset the bit counter and data register  
                bit_count <= 8'b0;
                data_reg <= 64'h0;
            end
            else begin
                 bit_count <= bit_count;			 // Maintain the bit counter if not done
            end
                bit_count <= bit_count + 1'b1;	     // Increment bit count
                data_reg[bit_count]<=data_in; 	     // Store the incoming data bit 
           end
    end  
    
    // Sequential logic block to monitor the completion of data reception and validate parity
    always@(posedge clk or negedge reset_n) begin
    	if(!reset_n) begin			 	             // Reset all registers on reset_n (active low)
    	   count <= 8'd0;					         // Clear the bit counter
           done <= 1'b0;					         // Deassert done signal
           data_out <= 64'h0;					     // Clear the output data register					 
        end 
        else if(count >= bit_count && count != 8'd0) begin       // When the count reaches the bit_count, assert done and output the data
           done <= 1'b1;				             // Set done to indicate transmission complete
           case(sel)
            3'b000:begin
                data_out <= 64'h0;                   // no bits to transmit
            end
            3'b001:begin
                data_out <= {56'h0,data_reg};        // 8 bits of data_reg 
                parity_error <= ~(parity_bit ^ data_reg[7]); // Check parity error
            end
            3'b010:begin
                data_out <= {48'h0,data_reg};        // 16 bits of data_reg
                parity_error <= ~(parity_bit ^ data_reg[15]); // Check parity error
            end
            3'b011:begin
                data_out <= {32'h0,data_reg};        // 32 bits of data_reg
                parity_error <= ~(parity_bit ^ data_reg[31]); // Check parity error
            end
            3'b100:begin
                data_out <= data_reg;                // 64 bits of data_reg
                parity_error <= ~(parity_bit ^ data_reg[63]); // Check parity error
            end
            default:begin
                data_out <= 64'h0;                   // no bits to transmit, default state
                parity_error <= 1'b0;
            end
           endcase
        end
        else if(bit_count > 8'd0) begin              // If bits are being received, increment the count and deassert done
           count <= count+ 1'b1;			     // Increment count
           done <= 1'b0;					         // Clear done signal while receiving
        end
        else begin  			                     // If no bits have been received, reset the count and deassert done
           count <= 8'd0;					         // Reset count
           done <= 1'b0;					         // Clear done signal   
        end 
    end

endmodule
