module morse_encoder(
    input [7:0] ascii_in,
    output reg [9:0] morse_out,
    output reg [3:0] morse_length
);

    // Morse code mapping table
    localparam [7:0] ascii_to_morse_map [0:255] = {
        // A
        0x41: {10'b01000000, 2},
        // B
        0x42: {10'b10000000, 4},
        // C
        0x43: {10'b10100000, 4},
        // D
        0x44: {10'b10000000, 3},
        // E
        0x45: {10'b00000000, 1},
        // F
        0x46: {10'b00100000, 4},
        // G
        0x47: {10'b11000000, 3},
        // H
        0x48: {10'b00000000, 4},
        // I
        0x49: {10'b00000000, 2},
        // J
        0x4A: {10'b01110000, 4},
        // K
        0x4B: {10'b10100000, 3},
        // L
        0x4C: {10'b01000000, 4},
        // M
        0x4D: {10'b00000000, 2},
        // N
        0x4E: {10'b10000000, 2},
        // O
        0x4F: {10'b11100000, 3},
        // P
        0x50: {10'b01100000, 4},
        // Q
        0x51: {10'b11010000, 4},
        // R
        0x52: {10'b01000000, 3},
        // S
        0x53: {10'b00000000, 3},
        // T
        0x54: {10'b00000000, 1},
        // U
        0x55: {10'b00010000, 3},
        // V
        0x56: {10'b00001000, 4},
        // W
        0x57: {10'b01100000, 3},
        // X
        0x58: {10'b10010000, 4},
        // Y
        0x59: {10'b10110000, 4},
        // Z
        0x5A: {10'b11000000, 4},
        // 0
        0x30: {10'b11111000, 5},
        // 1
        0x31: {10'b01111000, 5},
        // 2
        0x32: {10'b00111000, 5},
        // 3
        0x33: {10'b00011000, 5},
        // 4
        0x34: {10'b00001000, 5},
        // 5
        0x35: {10'b00000000, 5},
        // 6
        0x36: {10'b10000000, 5},
        // 7
        0x37: {10'b11000000, 5},
        // 8
        0x38: {10'b11100000, 5},
        // 9
        0x39: {10'b11110000, 5},
        // Unsupported ASCII
        255: {10'b00000000, 0}
    };

    always @* begin
        case (ascii_in)
            // Mapping ASCII to Morse
            ascii_to_morse_map[(ascii_in >> 3) * 8 + ascii_in & 7]: {morse_out, morse_length};
            default: {morse_out, morse_length} = 10'b00000000;
        endcase
    end

endmodule
